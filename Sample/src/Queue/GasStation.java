	public static int canCompleteCircuit(int[] A, int[] B) {
		int gas = 0;
		int cost = 0;
		int ans = 0;
		int total = 0;
		for (int i = 0; i < A.length; i++) {
			gas += A[i];
			cost += B[i];
			total += A[i] - B[i];
			if (total < 0) {
				ans = i + 1;
				total = 0;
			}
		}
		if (gas < cost) {
			return -1;
		} else {
			return ans;
		}
	}

